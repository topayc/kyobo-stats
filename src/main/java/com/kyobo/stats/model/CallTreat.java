package com.kyobo.stats.model;

public class CallTreat {
	private String TreatID;
	private String TreatCode;
	private String TreatName;
	private String TreatType;
	public String getTreatID() {
		return TreatID;
	}
	public void setTreatID(String treatID) {
		TreatID = treatID;
	}
	public String getTreatCode() {
		return TreatCode;
	}
	public void setTreatCode(String treatCode) {
		TreatCode = treatCode;
	}
	public String getTreatName() {
		return TreatName;
	}
	public void setTreatName(String treatName) {
		TreatName = treatName;
	}
	public String getTreatType() {
		return TreatType;
	}
	public void setTreatType(String treatType) {
		TreatType = treatType;
	}
}
